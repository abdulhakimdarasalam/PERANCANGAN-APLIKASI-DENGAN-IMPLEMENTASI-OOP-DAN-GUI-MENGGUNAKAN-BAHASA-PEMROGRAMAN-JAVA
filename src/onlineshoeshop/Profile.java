    public void disconnect() {
        try {
            if (con != null && !con.isClosed()) {
                con.close();
                System.out.println("Connection closed!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void pullFromDB(Connection con, String username) {
        try {
            String query = "SELECT * FROM akun WHERE username = ?";
            PreparedStatement preparedStatement = con.prepareStatement(query);
            preparedStatement.setString(1, username);
            
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                userN = resultSet.getString("username");
                tglL = resultSet.getString("tglLahir");
                NHP = resultSet.getString("no_hp");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void isiProfile(Connection con, String username) {        
        koneksiDatabase();
        Login lg = new Login();
        pullFromDB(getConnection(), lg.usernameLogin);
        txtUsername.setText(userN);
        txtTglLahir.setText(tglL);
        txtNoHp.setText(NHP);
        
        txtUsername.setEditable(false);
        txtTglLahir.setEditable(false);
        txtNoHp.setEditable(false);
    }

