    private void saveToDatabase(Connection conn, String username) {
        try {
            // Query untuk menyimpan data ke tabel akun
            String query = "UPDATE akun SET nama = ?, tgLahir = ?, no_hp = ? WHERE username = "+username;

            // Persiapan statement
            PreparedStatement preparedStatement = conn.prepareStatement(query);
            preparedStatement.setString(1, this.txtNama.getText());
            preparedStatement.setString(2, this.txtTglLahir.getText());
            preparedStatement.setString(3, this.txtNoHp.getText());
            // Eksekusi query
            int rowsAffected = preparedStatement.executeUpdate();
            if (rowsAffected > 0) {
                System.out.println("Data inserted successfully!");
            } else {
                System.out.println("Failed to insert data!");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
